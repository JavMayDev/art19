		    <p>
    No podemos pasar por alto que cada vez es m&#xE1;s evidente la insuficiencia de analizar la violencia contra la prensa por tipo de agresor, como si fueran actores completamente independientes entre s&#xED;. Las acciones m&#xE1;s violentas contra la prensa  tienen como principales perpetradores a &#x201C;particulares&#x201D;, &#x201C;desconocidos&#x201D; o integrantes del &#x201C;crimen organizado&#x201D;. Pero no debemos perder de vista que existen una multiplicidad de relaciones de contubernio y omisiones estatales que entra&#xF1;an esquemas de criminalidad m&#xE1;s complejos. 
		    </p>
		</div>
