		    <p>
    En 2020, registramos que seis periodistas fueron asesinados por motivos posiblemente vinculados con su labor: <a target="_blank" href="https://articulo19.org/asesinan-en-veracruz-a-la-periodista-maria-elena-ferral/">Mar&#xED;a Elena Ferral</a>, <a target="_blank" href="https://articulo19.org/asesinan-al-reportero-julio-valdivia-en-veracruz-el-estado-mas-letal-para-la-prensa/">Julio Valdivia</a>, <a target="_blank" href="https://articulo19.org/article-19-documenta-el-homicidio-de-comunicador-pablo-morrugares-en-guerrero/">Pablo Morrugarr&#xE9;s</a>, <a target="_blank" href="https://articulo19.org/jorge-miguel-armenta-ramos-director-de-medios-obson-es-asesinado-en-cajeme-sonora/">Jorge Armenta</a>, <a target="_blank" href="https://articulo19.org/autoridades-deben-investigar-asesinato-de-israel-vazquez-y-abstenerse-de-revictimizarlo/">Israel V&#xE1;zquez</a> y <a target="_blank" href="https://articulo19.org/asesinan-a-jaime-castano-tras-cobertura-fotografica-en-jerez-zacatecas/">Jaime Daniel Casta&#xF1;o</a>. Todos con precedentes de alg&#xFA;n tipo, incluso amenazas previas en cuatro casos. Y tres de ellos ultimados hall&#xE1;ndose al amparo de alguna medida de protecci&#xF3;n del Estado. As&#xED;, se llega a la lamentable cifra de 17 periodistas asesinados en lo que va de la administraci&#xF3;n actual. 
		    </p>
