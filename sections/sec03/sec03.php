	<div class="cover-title" style="top: 200px; left: 10vw;">
	    <h1 data-family="'Bronxos'" class="distor">03</h1>
	    <h1 class="distor distor-bg">El permiso</h1>
	    <h1 class="distor distor-bg">para agredir</h1>
	    <h1 class="distor distor-bg">a la prensa</h1>
	</div>
    </div>

    <div class="info">
	<div class="center columns">
	    <div>
		<p>
		L&#xF3;pez Obrador se queja de manera recurrente de que es el presidente m&#xE1;s &#x201C;atacado&#x201D; por la prensa para, despu&#xE9;s, magn&#xE1;nimo, <a target="_blank" href="https://www.eluniversal.com.mx/nacion/pese-insultos-amlo-garantiza-libertad-de-expresion">decir que no va a censurar ni a perseguir a nadie</a>
. 
		</p>
		<p>
Efectivamente, existen m&#xFA;ltiples discursos cr&#xED;ticos contra el gobierno federal en los medios convencionales. Pero esto es sano y necesario en una democracia, no puede considerarse una concesi&#xF3;n graciosa ni, mucho menos, puede ser materia de reproche por parte de las altas autoridades del pa&#xED;s.
		</p>
		<p>
Tal vez, el presidente no mande acallar periodistas de manera directa, pero los fustiga y estigmatiza, pasando por alto dos aspectos particulares: el ser un funcionario p&#xFA;blico sujeto a escrutinio y cr&#xED;tica p&#xFA;blicos, naturalmente intensos por el cargo que ocupa, y la situaci&#xF3;n tan apremiante de violencia que padece la prensa. 
		</p>
	    </div>
	    <div>
		<img src="sections/sec03/images/03.png" alt="">
	    </div>
	</div>
    </div>

    <div class="info">
	<div class="center columns">
	    <div>
		<p>
Los atentados contra los medios aumentaron un 13.63% respecto a 2019, arrojando un registro de 692 ataques en <strong>Una agresi&#xF3;n contra la prensa cada 13 horas</strong>
		</p>
		<img src="sections/sec03/images/03.1.png" alt="">
	    </div>
	    <div>
		<p>
De las agresiones contra periodistas, 49.5% fueron perpetradas por agentes del Estado (personas funcionarias p&#xFA;blicas, fuerzas de seguridad civil y fuerzas armadas). Es decir, un incremento de 29.4% en este tipo de ataques respecto a 2019. 
		</p>
		<p>
Las agresiones m&#xE1;s comunes contra la prensa cometidas por personas funcionarias p&#xFA;blicas fueron el uso ileg&#xED;timo del poder p&#xFA;blico, con 28.72% (principalmente hostigamientos judiciales y estigmatizaciones contra la prensa; intimidaciones y hostigamientos, con 23.94%, y bloqueos o alteraciones de contenido, con 21.28%. 
		</p>
		<p>
Las agresiones que provienen, espec&#xED;ficamente, de las fuerzas de seguridad civiles, que constituyen 144 de un total de 343 cometidas por autoridades (41.98%)
		</p>
		<p>
Llama la atenci&#xF3;n el aumento exponencial de los actos categorizados por ARTICLE 19 como &#x201C;uso ileg&#xED;timo del poder p&#xFA;blico&#x201D;. Los actos de hostigamiento administrativo y judicial registrados pasaron de 11, durante 2019, a 33, en 2020. Es decir, se triplic&#xF3; la utilizaci&#xF3;n del aparato de poder para amedrentar o acallar voces cr&#xED;ticas. 
		</p>
		<p>
Un caso paradigm&#xE1;tico es el de Notimex. Han pasado diez meses desde que dimos a conocer que directivas de esta agencia <a target="_blank" href="https://articulo19.org/directiva-de-notimex-ataca-periodistas-y-organiza-campanas-de-desprestigio-en-redes-sociales/">ordenan a sus empleados el uso de cuentas de Twitter para agredir a periodistas</a>, pero las autoridades a cargo de investigar estos hechos han actuado con inexplicable y obsequiosa lentitud. La respuesta del presidente sigue siendo ambivalente y la SFP mantiene estancadas las pesquisas. Mientras tanto, la <a target="_blank" href="https://articulo19.org/cndh-omite-investigar-violaciones-de-derechos-humanos-e-incumple-con-sus-obligaciones/">Comisi&#xF3;n Nacional de Derechos Humanos (CNDH) consider&#xF3; estas agresiones como un &#x201C;conflicto laboral&#x201D;</a>, enviando nuestra queja a la Sexta Visitadur&#xED;a, encargada, precisamente, de los casos de derechos laborales. La degradaci&#xF3;n de la agencia se ha agudizado con la huelga, que est&#xE1; lejos de resolverse por falta de voluntad de las autoridades de Notimex. 
		</p>
	    </div>
	</div>
    </div>

    <div class="info" getin="toggleBarsSec03Chart1">
	<div class="center">
	    <div class="columns">
		<div>
		    <p>
En voz del presidente, se ha repetido hasta el cansancio que ya no hay violaciones a los derechos humanos y que no se permitir&#xE1; la corrupci&#xF3;n ni la impunidad. Notimex es la prueba palmaria de que lo dicho en la palestra por L&#xF3;pez Obrador no se corresponde con los hechos de su gobierno.
		    </p>
		    <p>
El 2020 fue un a&#xF1;o de toma de espacios p&#xFA;blicos por las mujeres mexicanas, quienes, hartas de la violencia sist&#xE9;mica que viven d&#xED;a con d&#xED;a, salieron a las calles para reclamar los derechos que el sistema patriarcal violenta o, de plano, les niega. As&#xED;, en la cobertura de las movilizaciones feministas, <a target="_blank" href="https://articulo19.org/marcha-historica-del-8m-mujeres-toman-las-calles-para-resistir-a-la-violencia-y-policias-actuan-indebidamente/">periodistas mujeres fueron agredidas tanto por los cuerpos policiales como por particulares</a>, resaltando a&#xFA;n m&#xE1;s el doble riesgo de ser mujer y ser periodista en M&#xE9;xico.
		    </p>
		</div>
		<div>
		    <p>
Tambi&#xE9;n es importante recalcar que 191 de las 692 agresiones contra la prensa documentadas, es decir, 27.6%, son de car&#xE1;cter digital. Resaltan los <a target="_blank" href="https://articulo19.org/animal-politico-sufre-su-quinto-ataque-informatico-solo-durante-mayo/">seis ataques inform&#xE1;ticos para inhabilitar las suscripciones al portal Animal Pol&#xED;tico</a>, as&#xED; como los cometidos contra los medios de comunicaci&#xF3;n sinaloenses 
<a target="_blank" href="https://articulo19.org/organizaciones-y-periodistas-exigimos-investigacion-por-ataques-informaticos-a-medios-en-sinaloa/">Noroeste, Revista Espejo, Reacci&#xF3;n Informativa, Riodoce y L&#xED;nea Directa</a>, los cuales vieron sus sitios web desbordados por miles de peticiones de servicio (ataque DDOS) que limitaron el acceso a sus p&#xE1;ginas.
		    </p>
		    <p>
Con un total de 113 agresiones, el 16.3% de los ataques contra la prensa en 2020 fueron con motivo de la cobertura de la situaci&#xF3;n causada por el COVID-19. La llegada de la enfermedad a M&#xE9;xico implic&#xF3; tambi&#xE9;n un intento de censura en contra de quienes buscaban ejercer su derecho a informar sobre la situaci&#xF3;n real del manejo de la emergencia sanitaria y de la crisis econ&#xF3;mica que desatar&#xED;a en el pa&#xED;s.
		    </p>
		</div>
